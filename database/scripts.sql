-- Tabela de atendimentos IoT
CREATE TABLE ATENDIMENTOS_IOT (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    ID_ATENDIMENTO NUMBER,
    USUARIO VARCHAR2(100),
    STATUS VARCHAR2(50),
    DURACAO_MIN NUMBER,
    DATA_HORA TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

